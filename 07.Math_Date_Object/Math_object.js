// Math Object program
// Math.PI, Math.abs()
// Math.sin(), Math.cos() ...
// Math.pow(x,y), Math.sqrt()
// Math.floor(), Math.ceil(), Math.round(), 
// Math.min(), Math.max()

document.write("<br>"+"<br>"+"Math Object.............................."+"<br>"+"<br>");

document.write(Math.sqrt(25));
document.write("<br>");
// sin(), cos(), tan() ...
document.write(Math.sin(25));
document.write("<br>");

document.write(Math.abs(-25));
document.write("<br>");
document.write(Math.pow(2, 3));
document.write("<br>");
document.write(Math.floor(2.4));
document.write("<br>");
document.write(Math.ceil(2.4));
document.write("<br>");
document.write(Math.round(2.4));
document.write("<br>");
document.write(Math.max(2, 8));
document.write("<br>");
document.write(Math.min(2, 8));
document.write("<br>");
// returns value between 0 to 1 by default
document.write(Math.random());
document.write("<br>");
// returns value between 0 to 5 
document.write(Math.random() * 6);
document.write("<br>");
// returns value between 0 to 5 integer value
document.write(Math.floor(Math.random() * 6));
document.write("<br>");
// returns value between 1 to 5 integer value
document.write(Math.floor(Math.random() * 5) + 1);
document.write("<br>");